// Autoboxing in JavaScript
// Autoboxing is the automatic conversion of a primitive type to its corresponding object type when a method is called on it.
// when a dot appears after a primitive type, it is automatically converted to an object type
let str = "hello";
console.log(str.toUpperCase());  //  ->  HELLO




